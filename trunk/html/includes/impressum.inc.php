<h2>Distanzierung von Inhalten verlinkter Sites</h2>
Das Landgericht Hamburg hat am 12. Mai 1998 (312 O 85/98 - "Haftung für Links") entschieden,
dass die Anbringung eines Links auch die Übernahme der Verantwortung für die Inhalte der
gelinkten Seite ggf. einschließt. Dies gilt aber dann nicht, wenn sich der Betreffende
ausdrücklich von diesen Inhalten distanziert.
<p>
Holy-Wars 2 distanziert sich daher ausdrücklich von allen Inhalten auf Sites, die auf
www.holy-wars2.de und alle hiervon wegführenden Hauptdomains gelinkt werden. Holy-Wars 2
macht sich diese Inhalte ausdrücklich nicht zu eigen.
